export async function POST(request: NextRequest) {
  try {
    const { message, vehicle, language = 'ar' } = await request.json();

    if (!message) {
      return NextResponse.json({
        success: false,
        error: language === 'ar' ? 'الرسالة مطلوبة' : 'Message is required'
      }, { status: 400 });
    }

    const isArabic = language === 'ar';
    const responseMap = responses[language] || responses.ar;
    
    // Find matching response
    let response = '';
    
    for (const [question, responseFunc] of Object.entries(responseMap)) {
      if (message.includes(question) || question.includes(message.substring(0, 20))) {
        response = responseFunc(vehicle);
        break;
      }
    }
    
    // If no specific response found, use generic
    if (!response) {
      response = getGenericResponse(vehicle, language, message);
    }

    // Simulate processing delay
    await new Promise(resolve => setTimeout(resolve, 500));

    return NextResponse.json({
      success: true,
      response,
      timestamp: new Date().toISOString()
    });

  } catch (error) {
    console.error('Chat error:', error);
    return NextResponse.json({
      success: false,
      error: 'حدث خطأ. الرجاء المحاولة مرة أخرى.',
      errorEn: 'An error occurred. Please try again.'
    }, { status: 500 });
  }
}
